    private void llenarInventario(){
        String[] datos = new String[6];
        dtm.setColumnIdentifiers(titulos);
        inventarioTable.setModel(dtm);
        
        try{
           Statement statement = Menu.conn.createStatement();
           ResultSet result = statement.executeQuery(sql);
           
           while(result.next()){
               datos[0] = String.valueOf(result.getInt(1));
               datos[1] = result.getString(2);
               datos[2] = result.getString(3);
               datos[3] = String.valueOf(result.getDouble(4));
               datos[4] = String.valueOf(result.getInt(5));
               datos[5] = result.getString(6);
               
               dtm.addRow(datos);
           }
           
        }catch(SQLException ex){
           ex.printStackTrace();
        }
    }
    
